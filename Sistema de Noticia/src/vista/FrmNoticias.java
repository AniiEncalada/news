    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (Utilidades.isEmpty(txtTitulo.getText()) || Utilidades.isEmpty(txtContenido.getText())) {
            UtilidadesComponente.mensajeError("Error", "Todos los campos son obligatorios");
        } else {
            cargarObjeto();
            if (sn.obtenerNoticia().getId() != null) {
                //Modificar
                if (sn.guardar()) {
                    UtilidadesComponente.mensajeOK("Aviso", "Se ha modificado correctamente");
                    limpiar();
                    cargarTabla();
                } else {
                    UtilidadesComponente.mensajeError("Error", "No se ha podido modificar");
                }
            } else {
                //Guardar
                if (sn.guardar()) {
                    UtilidadesComponente.mensajeOK("Aviso", "Se ha guardado correctamente");
                    limpiar();
                    cargarTabla();
                } else {
                    UtilidadesComponente.mensajeError("Error", "No se ha podido guardar");
                }
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

