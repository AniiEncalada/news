    private void cargarTabla() {
        modelo.setLista(sn.listarNoticiaAdministrador(Sesiones.getSesion().getId()));
        tblNoticias.setModel(modelo);
        tblNoticias.updateUI();
    }

    private void cargarCombo() {
        UtilidadesComponente.cargarComboTipo(cbxTipoNoticia);
        cbxTipoNoticia.updateUI();
    }

    private void buscar() {
        if (Utilidades.isEmpty(txtBuscar.getText())) {
            cargarTabla();
        } else {
            if (txtBuscar.getText().trim().length() >= 3) {
                modelo.setLista(sn.listarNoticiaAdministradorBusqueda(Sesiones.getSesion().getId(), txtBuscar.getText()));
                tblNoticias.setModel(modelo);
                tblNoticias.updateUI();
            }
        }
    }

    private void cargarObjeto() {
        sn.obtenerNoticia().setContenido(txtContenido.getText());
        sn.obtenerNoticia().setFecha(new Date());
        sn.obtenerNoticia().setTitulo(txtTitulo.getText());
        sn.obtenerNoticia().setTipoNoticia((TipoNoticia) cbxTipoNoticia.getSelectedItem());
        ServicioAdministrador sa = new ServicioAdministrador();
        sa.obtenerAdministrador().setId(Sesiones.getSesion().getId());
        sn.obtenerNoticia().setAdministrador(sa.obtenerAdministrador());
    }

    private void limpiar() {
        sn.fijarInstancia(null);
        txtTitulo.setText(null);
        txtContenido.setText(null);
        txtBuscar.setText(null);
        cbxTipoNoticia.setSelectedIndex(0);
    }

    private void cargarEdicion() {
        int fila = -1;
        fila = tblNoticias.getSelectedRow();
        if (fila < 0) {
            UtilidadesComponente.mensajeError("Error", "Se debe escoger una Noticia de la tabla");
        } else {
            sn.fijarInstancia(modelo.getLista().get(fila));
            txtContenido.setText(sn.obtenerNoticia().getContenido());
            txtTitulo.setText(sn.obtenerNoticia().getTitulo());
            cbxTipoNoticia.setSelectedItem(sn.obtenerNoticia().getTipoNoticia());
        }
    }

